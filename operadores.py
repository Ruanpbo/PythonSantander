## Aritméticos
# Soma (+): Soma dois valores
# Subtração (-): Subtrai dois valores
# Multiplicação (*): multiplica dois valores
# Divisão (/): Divide o primeiro valor pelo segundo e devolve um resultado de tipo flutuante
# Divisão inteira (//): Divie o primeiro valor pelo segundo e devolve um resultado de tipo inteiro (a parte decimal é descartada)
# Módulo (%): Devolve o resto da divisão entre o primeiro valor e o segundo
# Exponenciação (**): Eleva o primeiro valor á potencia do segundo

# Exemplos:

a = 10
b = 3

soma = a + b
subtracao = a - b 
multiplicacao = a * b
divisao = a / b 
divisaoInteira = a // b
modulo =  a % b
exponenciacao = a ** b

## Comparação

# Igual a (==): devolve tru se ambos os valores forem iguais
# Diferente de (!=) devolve true se os valores forem diferente
# Maio que (>) devolve true se o primeiro valor é maior que o segundo
# Menor que (<) devolve true se o primeiro valor é menor que o segundo
# Maior ou igual que (>=) devolve true se o primeiro valor é maior ou igual que o segundo
# menor ou igual que (<=) devolve true se o primeiro valor é menor ou igual que o segundo

#Exemplos

igual = a == b
diferente = a != b
maiorQue = a > b
menorQue = a < b
maiorOuIgual = a >= b
menorOuIgual = a <= b

## Logicos

# AND (and) devolve true se ambas as condições forem verdadeiras 
# OR (or) devolve true se ao menos uma das condições é verdadeira
# NOT (not) inverte o valor de uma condição, devolve true se a condição for falsa e False se a condição é verdadeira

#Exemplos

resultadoAnd = (a>5) and (b<5)
resultadoOr = (a > 15) or (b < 5)
resultadoNot = not (a > 5)